a=3
b=7

#Conditional Statements

#equals(==)
if a==b:
    print("a is equal to b")
    
#not equals(!=)
if a!=b:
    print("a is not equal to b")


#less than (<)
if a<b:
    print("a is less than b")
    
#less than equal to (<=)
if a<=b:
    print("a is either less than or equals to b")
        
#greater than (>)
if a>b:
    print("a is greater than b")
            
#greater than equal to(>=)
if a>=b:
    print("a is either greater than or equals b")
                
               
                    
                    
